/*
 * Type 0 configuration space layout.
 */
struct pci_dev {
	ushort_t id_vendor;
	ushort_t id_product;
	ushort_t cmd;
	ushort_t status;
	ubyte_t id_rev;
	ubyte_t class_code[3];
	ubyte_t cacheline;
	ubyte_t timer;
	ubyte_t type;
	ubyte_t bist;
	uint_t base_regs[6];
	uint_t cardbus;
	ushort_t id_vendor_sub;
	ushort_t id_product_sub;
	uint_t xrom_base;
	ubyte_t cap_ptrs;
	ubyte_t reserved_1[3];
	uint_t reserved_2;
	ubyte_t int_line;
	ubyte_t int_pin;
	ubyte_t min_gnt;
	ubyte_t max_lat;
	ubyte_t non_pre_hdr[192]; // device-specific bytes
};

/*
 * Type 1 configuration space layout.
 */
struct pci_brg {
	ushort_t id_vendor;
	ushort_t id_product;
	ushort_t cmd;
	ushort_t status;
	ubyte_t id_rev;
	ubyte_t class_code[3];
	ubyte_t cacheline;
	ubyte_t timer;
	ubyte_t type;
	ubyte_t bist;
	uint_t base_regs[2];
	ubyte_t pri_bus_num;
	ubyte_t sec_bus_num;
	ubyte_t sub_bus_num;
	ubyte_t sec_lat_timer;
	ubyte_t io_base;
	ubyte_t io_limit;
	ushort_t sec_status;
	ushort_t mem_base;
	ushort_t mem_limit;
	ushort_t mem_prftch_base;
	ushort_t mem_prftch_limit;
	uint_t prftch[2];
	ushort_t io_up_base;
	ushort_t io_up_limit;
	ubyte_t cap_ptrs;
	ubyte_t reserved_1[3];
	uint_t xrom_base;
	ubyte_t int_line;
	ubyte_t int_pin;
	ushort_t brdg_ctrl;
	ubyte_t non_pre_hdr[192]; // device-specific bytes
};


/*
 * Since all configuration space layouts are equal size,
 * just union them here.
 */
struct pci_conf {
	union pci_lout {
		struct pci_dev pci_dev;
		struct pci_brg pci_brg;
	} pci_lout;
};

struct pci_bus {
	ubyte_t bus_num;
	ubyte_t bus_sub;
	struct pci_bus *bus_next;
};
